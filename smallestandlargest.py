first_number = float(input("Enter the first number: "))
second_number = float(input("Enter the second number: "))
third_number = float(input("Enter the third number: "))

product = first_number * second_number * third_number
addition = first_number + second_number + third_number
num_list = (first_number,second_number,third_number)
average = sum(num_list)/len(num_list)
smallest = min(num_list)
largest = max(num_list)
print(f"The product of {first_number} and {second_number} and {third_number} is = {product} \nThe addition of {first_number} and {second_number} and {third_number} is = {addition} \nThe average of {first_number} and {second_number} and {third_number} is = {average} \nThe smallest of {first_number} and {second_number} and {third_number} is = {smallest} \nThe largest of {first_number} and {second_number} and {third_number} is = {largest}")
 
 
 
 
 
